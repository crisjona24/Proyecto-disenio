    //METODOS PARA MANEJAR LAS TABLAS
     public void cargarTorneo() {
        Torneo torneo = null;
        if (this.tablaTorneo.isEnabled()) {
            int selectedRow = this.tablaTorneo.getSelectedRow();
            
            String nombre = modelo.getValueAt(selectedRow, 0).toString();
            
            //buscamos a ese torneo para traer su id
            torneo = control.obtenerTorneoNombre(nombre);
            Integer num = torneo.getNumeroEquipos();
            control.id_tor = torneo;
            control.numEquipos = num;
            
        }
    }

    public void tablaModelo() {
        //damos los parametros que se veran reflejados en la tabla
        tablaTorneo.getColumnModel().getColumn(0).setPreferredWidth(40);
        tablaTorneo.getColumnModel().getColumn(0).setPreferredWidth(40);
        
        modelo = (DefaultTableModel) tablaTorneo.getModel();
        modelo.setNumRows(0);
    }

    public void cargatabla() {
        //a√±adimos a traves de una lista los datos a la tabla
        List<Torneo> lista = control.listarTorneo();

        for (Torneo actlist : lista) {
            
            String nombre = String.valueOf(actlist.getNombreTorneo());
            String a = String.valueOf(actlist.getNumeroEquipos());   
            modelo.addRow(new Object[]{ nombre, a,});
        }
    }

